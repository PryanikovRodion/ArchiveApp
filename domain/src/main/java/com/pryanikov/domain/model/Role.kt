package com.pryanikov.domain.model

/**
 * Enum, що представляє всі можливі ролі користувача в системі.
 */
enum class Role {
    ADMIN,    // Адміністратор
    EDITOR,   // Редактор
    READER,   // Читач (наприклад, гість або співробітник іншого відділу)
    UNKNOWN   // Невідома роль (для безпечної обробки даних)
}